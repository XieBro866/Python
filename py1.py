import turtle as t

t.shape("turtle")
t.color("red")
t.pensize(1)
t.speed(1)
t.setup(800,800)
t.penup()
t.goto(-200, 300)
t.pendown()
t.right(30)
t.fd(600)
t.right(120)
t.fd(600)
t.right(120)
t.fd(600)
t.penup()
t.fd(-300)
t.left(90)
t.fd(173.2)
t.pendown()
t.color("green")
t.right(210)
t.fd(600)
t.left(120)
t.fd(600)
t.left(120)
t.fd(600)
t.penup()
    
t.done()

